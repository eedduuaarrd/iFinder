export function detectInitialLang(): Lang {
  if (typeof window === "undefined") return "ca";
  const stored = window.localStorage.getItem("flashhunt.lang");
  if (stored === "ca" || stored === "es" || stored === "en") return stored;
  const nav = (window.navigator.language ?? "ca").slice(0, 2).toLowerCase();
  if (nav === "ca") return "ca";
  if (nav === "es") return "es";
  return "en";
}
